    List searchResult = [];
    if (query.isEmpty) {
      // if the search field is empty or only contains white-space, we'll display all users
      searchResult = allQuote;
    } else {
      searchResult =
          allQuote.where((element) => element.contains(query)).toList();
    }
    return searchResult;
  }
